int e3gai::fastDual(const e3gai &a) {
	setUsage(a.usage);
	int gua = a.gradeUsage(), gud = 0;
	int ia = e3gai_mvSize[gua], id = 0;
	const double *ca = a.c;
	double *cd = c;
	if (gua & GRADE3) {
		gud |= GRADE0;
		ia -= e3gai_gradeSize[3];
		cd[id + 0] = ca[ia + 0];
		id += e3gai_gradeSize[0];
	}
	if (gua & GRADE2) {
		gud |= GRADE1;
		ia -= e3gai_gradeSize[2];
		cd[id + 0] = ca[ia + 0];
		cd[id + 1] = ca[ia + 1];
		cd[id + 2] = ca[ia + 2];
		id += e3gai_gradeSize[1];
	}
	if (gua & GRADE1) {
		gud |= GRADE2;
		ia -= e3gai_gradeSize[1];
		cd[id + 0] = -ca[ia + 0];
		cd[id + 1] = -ca[ia + 1];
		cd[id + 2] = -ca[ia + 2];
		id += e3gai_gradeSize[2];
	}
	if (gua & GRADE0) {
		gud |= GRADE3;
		ia -= e3gai_gradeSize[0];
		cd[id + 0] = -ca[ia + 0];
		id += e3gai_gradeSize[3];
	}

	usage = gud | (usage ^ gua);
	return 0;
}

