void e3gai::expand(const double *pa[4]) const {
	int ia = 0;
	int i, j = 1;
	for (i = 0; i < 4; i++) {
		if (gradeUsage() & j) {
			pa[i] = c + ia;
			ia += e3gai_gradeSize[i];
		}
		else pa[i] = NULL;
		j <<= 1;
	}
}

void e3gai::expand2i(const e3gai &b, double const *pa[4], double const *pb[4]) const {
	int ia = 0;
	int i, j = 1;
	for (i = 0; i < 4; i++) {
		if (gradeUsage() & j) {
			pa[i] = c + ia;
			pb[i] = b.c + ia;
			ia += e3gai_gradeSize[i];
		}
		else pa[i] = pb[i] = NULL;
		j <<= 1;
	}
}

void e3gai::expand(const e3gai &b, double const *pa[4], double const *pb[4]) const {
	int ia = 0, ib = 0;
	int i, j = 1;
	for (i = 0; i < 4; i++) {
		if (gradeUsage() & j) {
			pa[i] = c + ia;
			ia += e3gai_gradeSize[i];
		}
		else pa[i] = NULL;
		if (b.gradeUsage() & j) {
			pb[i] = b.c + ib;
			ib += e3gai_gradeSize[i];
		}
		else pb[i] = NULL;
		j <<= 1;
	}
}

void e3gai::compress(double coordinates[8]) {
	int i, j, k, l, s, u;
	double cc[8];
	l = k = u = 0;
	for (i = 0; i < 4; i++) {
		s = e3gai_gradeSize[i];
		for (j = 0; j < s; j++)
			if (coordinates[k + j] != 0.0f) {
				u |= (1 << i);
				e3gai_copy(cc + l, coordinates + k, s);
				l += s;
				break;
			}
		k += s;
	}
	setUsage(u | (l << 4));
	e3gai_copy(c, cc, l);
}

